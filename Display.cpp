// =============================================
// FUNÇÕES DE DESENHO E CONTROLE
// =============================================
void desenharOlhos() {
  telaEsquerda.clearDisplay();
  telaDireita.clearDisplay();

  telaEsquerda.drawBitmap(0, 0, bitmap_olhos, 128, 64, SSD1306_WHITE);
  telaDireita.drawBitmap(0, 0, bitmap_olhos, 128, 64, SSD1306_WHITE);

  telaEsquerda.display();
  telaDireita.display();
}

void inicializarDisplays(int &codigoErro) {
  // Inicializa Tela Esquerda
  if(!telaEsquerda.begin(SSD1306_SWITCHCAPVCC, ENDERECO_TELA_ESQUERDA)) {
    Serial.println("[DISPLAY] Falha critica: Tela Esquerda nao encontrada!");
    codigoErro |= ERR_DISPLAY_ESQ;
  } else {
    telaEsquerda.ssd1306_command(SSD1306_SETCONTRAST);
    telaEsquerda.ssd1306_command(1); 
    Serial.println("[DISPLAY] Tela Esquerda iniciada.");
  }

  // Inicializa Tela Direita
  if(!telaDireita.begin(SSD1306_SWITCHCAPVCC, ENDERECO_TELA_DIREITA)) {
    Serial.println("[DISPLAY] Falha critica: Tela Direita nao encontrada!");
    codigoErro |= ERR_DISPLAY_DIR;
  } else {
    telaDireita.ssd1306_command(SSD1306_SETCONTRAST);
    telaDireita.ssd1306_command(1); 
    Serial.println("[DISPLAY] Tela Direita iniciada.");
  }

  desenharOlhos();
}

void desligarDisplays() {
  telaEsquerda.ssd1306_command(SSD1306_DISPLAYOFF);
  telaDireita.ssd1306_command(SSD1306_DISPLAYOFF);
  Serial.println("[DISPLAY] Telas desligadas para o Deep Sleep.");
}

void atualizarTextoDisplays(String textoEsq, String textoDir) {
  telaEsquerda.clearDisplay();
  telaEsquerda.setTextSize(1);
  telaEsquerda.setTextColor(SSD1306_WHITE);
  telaEsquerda.setCursor(20, 30);
  telaEsquerda.println(textoEsq);
  telaEsquerda.display();

  telaDireita.clearDisplay();
  telaDireita.setTextSize(1);
  telaDireita.setTextColor(SSD1306_WHITE);
  telaDireita.setCursor(20, 30);
  telaDireita.println(textoDir);
  telaDireita.display();
}